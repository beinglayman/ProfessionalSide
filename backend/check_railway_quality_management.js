const { PrismaClient } = require('@prisma/client');
require('dotenv').config();

// IMPORTANT: Always use Railway production database
const prisma = new PrismaClient();

async function checkRailwayQualityManagement() {
  try {
    console.log('üöÇ CHECKING RAILWAY PRODUCTION: Operations > Quality Management');
    console.log('üìÖ Timestamp:', new Date().toISOString());
    console.log('üîó Database URL configured:', process.env.DATABASE_URL ? 'YES (Railway)' : 'NO - USING LOCAL');
    console.log('');

    if (!process.env.DATABASE_URL) {
      console.log('‚ùå ERROR: DATABASE_URL not configured - cannot connect to Railway');
      console.log('üí° Make sure Railway environment variables are loaded');
      return;
    }

    // Get Quality Management category in Operations from Railway
    const qualityManagementCategory = await prisma.workCategory.findFirst({
      where: {
        id: 'operations-quality-management'
      },
      include: {
        focusArea: true,
        workTypes: {
          include: {
            workTypeSkills: {
              include: { skill: true }
            }
          },
          orderBy: { label: 'asc' }
        }
      }
    });

    if (!qualityManagementCategory) {
      console.log('‚ùå Operations > Quality Management category not found in Railway database');
      return;
    }

    console.log(`‚úÖ Found in Railway: ${qualityManagementCategory.focusArea.label} > ${qualityManagementCategory.label}`);
    console.log(`üìã Work types in Railway: ${qualityManagementCategory.workTypes.length}`);
    console.log('');

    if (qualityManagementCategory.workTypes.length === 0) {
      console.log('‚ùå No work types found in Railway Quality Management category');
      return;
    }

    console.log('üöÇ RAILWAY PRODUCTION - QUALITY MANAGEMENT WORK TYPES:');
    console.log('');

    qualityManagementCategory.workTypes.forEach((workType, index) => {
      const skillCount = workType.workTypeSkills.length;
      const skills = workType.workTypeSkills.map(wts => wts.skill.name);
      
      console.log(`${index + 1}. ${workType.label}`);
      console.log(`   üÜî ID: ${workType.id}`);
      console.log(`   üìä Skills in Railway: ${skillCount}`);
      
      if (skillCount > 0) {
        console.log(`   ‚úÖ Railway mapped skills: ${skills.join(', ')}`);
      } else {
        console.log(`   ‚ùå No skills mapped in Railway production`);
      }
      console.log('');
    });

    // Summary
    const workTypesWithSkills = qualityManagementCategory.workTypes.filter(wt => wt.workTypeSkills.length > 0).length;
    const workTypesWithoutSkills = qualityManagementCategory.workTypes.length - workTypesWithSkills;

    console.log('üìà RAILWAY PRODUCTION - QUALITY MANAGEMENT SUMMARY:');
    console.log(`üìä Total work types: ${qualityManagementCategory.workTypes.length}`);
    console.log(`‚úÖ Work types with skills: ${workTypesWithSkills}`);
    console.log(`‚ùå Work types without skills: ${workTypesWithoutSkills}`);
    console.log(`üìà Coverage: ${qualityManagementCategory.workTypes.length > 0 ? ((workTypesWithSkills / qualityManagementCategory.workTypes.length) * 100).toFixed(1) : 0}%`);

    if (workTypesWithSkills === qualityManagementCategory.workTypes.length) {
      console.log('üéâ All Quality Management work types have skills mapped in Railway!');
    } else {
      console.log('‚ö†Ô∏è  Some Quality Management work types need skill mapping in Railway');
    }

  } catch (error) {
    console.error('‚ùå Error checking Railway Quality Management:', error);
    if (error.message.includes('connect')) {
      console.log('üí° Connection issue - check if Railway DATABASE_URL is correct');
    }
  } finally {
    await prisma.$disconnect();
  }
}

checkRailwayQualityManagement();